package com.game.goods;

/**
 * Top-level category for every good in the GoodsCatalog.
 *
 * VINE_STOCK  – rooted cuttings / grafted stock for planting
 * VESSEL      – fermentation / maturation containers (qvevri, barrel, tank)
 * EQUIPMENT   – machines and presses
 * INPUT       – consumable field/cellar inputs (sprays, seeds, netting)
 * TOOL        – durable hand tools
 */
public enum GoodCategory {
    VINE_STOCK,
    VESSEL,
    EQUIPMENT,
    INPUT,
    TOOL
}
